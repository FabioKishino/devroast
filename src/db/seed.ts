async function seed() {
  const client = postgres(process.env.DATABASE_URL as string);
  const db = drizzle(client, { casing: "snake_case" });

  console.log("🌱 Seeding database...");

  // Clear existing data
  await db.delete(diffSuggestions);
  await db.delete(analysisItems);
  await db.delete(submissions);
  console.log("  ✓ Cleared existing data");

  const TOTAL = 100;

  for (let i = 0; i < TOTAL; i++) {
    const language = faker.helpers.arrayElement(LANGUAGES);
    const codeLines = generateCodeSnippet(language);
    const roastMode = faker.datatype.boolean({ probability: 0.4 });
    // Skew scores low — it's a shame leaderboard
    const score = faker.number
      .float({ min: 0.5, max: 10, fractionDigits: 2 })
      .toFixed(2);
    const roastQuote = faker.helpers.arrayElement(ROAST_QUOTES);
    const createdAt = faker.date.between({
      from: new Date("2025-01-01"),
      to: new Date(),
    });

    const submission: NewSubmission = {
      code: codeLines.join("\n"),
      language,
      linesCount: codeLines.length,
      roastMode,
      score,
      roastQuote,
      createdAt,
    };

    const [inserted] = await db
      .insert(submissions)
      .values(submission)
      .returning({ id: submissions.id });

    // 2–4 analysis items per submission
    const itemCount = faker.number.int({ min: 2, max: 4 });
    const criticalPool = faker.helpers.shuffle([...CRITICAL_ISSUES]);
    const warningPool = faker.helpers.shuffle([...WARNING_ISSUES]);
    const goodPool = faker.helpers.shuffle([...GOOD_ISSUES]);

    const items: NewAnalysisItem[] = [];

    // Always at least 1 critical or warning
    const numCritical = faker.number.int({
      min: 1,
      max: Math.min(2, itemCount - 1),
    });
    const numWarning = faker.number.int({
      min: 0,
      max: Math.min(1, itemCount - numCritical),
    });
    const numGood = itemCount - numCritical - numWarning;

    for (let c = 0; c < numCritical; c++) {
      const issue = criticalPool[c % criticalPool.length];
      items.push({
        submissionId: inserted.id,
        severity: "critical",
        title: issue.title,
        description: issue.description,
        sortOrder: items.length,
      });
    }
    for (let w = 0; w < numWarning; w++) {
      const issue = warningPool[w % warningPool.length];
      items.push({
        submissionId: inserted.id,
        severity: "warning",
        title: issue.title,
        description: issue.description,
        sortOrder: items.length,
      });
    }
    for (let g = 0; g < numGood; g++) {
      const issue = goodPool[g % goodPool.length];
      items.push({
        submissionId: inserted.id,
        severity: "good",
        title: issue.title,
        description: issue.description,
        sortOrder: items.length,
      });
    }

    await db.insert(analysisItems).values(items);

    // Diff suggestions
    const diffLines = generateDiffLines(language);
    const diffRows: NewDiffSuggestion[] = diffLines.map((line, idx) => ({
      ...line,
      submissionId: inserted.id,
      lineNumber: idx + 1,
    }));
    await db.insert(diffSuggestions).values(diffRows);
  }

  console.log(
    `  ✓ Inserted ${TOTAL} submissions with analysis items and diff suggestions`
  );
  console.log("🎉 Seed complete!");

  await client.end();
}

seed().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});

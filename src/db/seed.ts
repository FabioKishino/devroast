type DiffLine = { lineType: "added" | "removed" | "context"; content: string };

function generateDiffLines(language: string): DiffLine[] {
  const diffs: Record<string, DiffLine[]> = {
    javascript: [
      { lineType: "context", content: "function calculateTotal(items) {" },
      { lineType: "removed", content: "  var total = 0;" },
      {
        lineType: "removed",
        content: "  for (var i = 0; i < items.length; i++) {",
      },
      { lineType: "removed", content: "    total = total + items[i].price;" },
      { lineType: "removed", content: "  }" },
      { lineType: "removed", content: "  return total;" },
      {
        lineType: "added",
        content: "  return items.reduce((sum, item) => sum + item.price, 0);",
      },
      { lineType: "context", content: "}" },
    ],
    typescript: [
      { lineType: "context", content: "function getUser(id: any): any {" },
      {
        lineType: "removed",
        content: "  return db.query('SELECT * FROM users WHERE id = ' + id);",
      },
      {
        lineType: "added",
        content:
          "  return db.query('SELECT * FROM users WHERE id = $1', [id]);",
      },
      { lineType: "context", content: "}" },
    ],
    python: [
      { lineType: "context", content: "def get_user(id):" },
      {
        lineType: "removed",
        content: "    query = 'SELECT * FROM users WHERE id = ' + str(id)",
      },
      {
        lineType: "added",
        content: "    query = 'SELECT * FROM users WHERE id = %s'",
      },
      { lineType: "added", content: "    return db.execute(query, (id,))" },
      { lineType: "removed", content: "    return db.execute(query)" },
    ],
    java: [
      { lineType: "context", content: "public User getUser(String id) {" },
      {
        lineType: "removed",
        content: '  String q = "SELECT * FROM users WHERE id = " + id;',
      },
      { lineType: "removed", content: "  return db.execute(q);" },
      { lineType: "added", content: "  return db.findById(id).orElseThrow();" },
      { lineType: "context", content: "}" },
    ],
  };

  const lines = diffs[language] ?? diffs.javascript;
  return lines;
}

